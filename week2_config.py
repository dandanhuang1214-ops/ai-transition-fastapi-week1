# ============================================================
# 数据库配置
# ============================================================
# SQLite 数据库文件路径。SQLite 是嵌入式数据库，数据存储在本地文件中，
# 无需安装额外的数据库服务器，非常适合学习和开发阶段。
# check_same_thread=False 是 SQLite + FastAPI 的常见配置，
# 因为 FastAPI 可能在多个线程中操作数据库。
DATABASE_URL = "sqlite:///week2_rag.db"
DATABASE_CONNECT_ARGS = {"check_same_thread": False}

# ============================================================
# FastAPI 服务地址
# ============================================================
# Streamlit 前端通过这个地址调用后端 API。
# 默认端口 8000，与 uvicorn 默认端口一致。
API_BASE_URL = "http://127.0.0.1:8000"

# ============================================================
# 文本切分配置（RAG 文档分块参数）
# ============================================================
# CHUNK_SIZE: 每个文本块的最大字符数。
#   太小 → 信息碎片化，检索时丢失上下文
#   太大 → 检索精度下降，容易引入无关内容
#   推荐范围：300-1000 字符，中文可偏小
CHUNK_SIZE = 500

# CHUNK_OVERLAP: 相邻两个文本块之间的重叠字符数。
#   重叠的作用：防止关键信息正好被切在两块边界上
#   推荐：chunk_size 的 10%-20%
CHUNK_OVERLAP = 50

# ============================================================
# 应用信息
# ============================================================
APP_TITLE = "个人知识库系统 — Week 2 实践"
APP_VERSION = "0.1.0"
APP_DESCRIPTION = (
    "第二周实践项目：FastAPI + SQLModel + Streamlit "
    "实现文档管理、文本切分、问答接口雏形"
)
